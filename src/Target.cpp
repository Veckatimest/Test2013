#include "Target.h"

Target::Target(FPoint startPos, FPoint startSpeed, float radius) : GameObject(startPos, startSpeed, radius) { }

/// Стены все прямые, так что усложнять не станем
/// если normal.x = -1, значит столкнулись с правой стенкой
/// если normal.x = 1, значит с левой
void Target::hitWall(FPoint normal)
{
	
	if ((normal.x > 0 && speed.x < 0) || // летели влево и врезались
			(normal.x < 0 && speed.x > 0))// летели вправо
		speed.x = -speed.x;
	if ((normal.y > 0 && speed.y < 0) || // ...вверх
			(normal.y < 0 && speed.y > 0))// ...вниз
		speed.y = -speed.y;
}